int		ft_iterative_factorial(int nb)
{
	int r;

	r = nb;
	if (r < 0 || r > 12)
	{
		return (0);
	}
	if (r == 0)
	{
		return (1);
	}
	while (--nb)
	{
		r *= nb;
	}
	return (r);
}
